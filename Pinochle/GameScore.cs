using System;
using System.Collections.Generic;
using System.Text;

namespace JFadich.Pinochle.Engine
{
    class GameScore
    {
        public int TeamA { get; private set; }
        public int TeamB { get; private set; }

        public void AddToTeamA(int score)
        {
            TeamA += score;
        }
        public void AddToTeamB(int score)
        {
            TeamB += score;
        }
    }
}
